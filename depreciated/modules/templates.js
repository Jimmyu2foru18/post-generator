// Add template combination utilities
export const templateUtils = {
    combineTemplates: (templates, topic) => {
        return templates.map(t => t.replace('${topic}', topic));
    },
    
    generateVariations: (template, replacements) => {
        return replacements.map(r => template.replace('${topic}', r));
    },
    
    randomizeTemplate: (templates) => {
        return templates[Math.floor(Math.random() * templates.length)];
    },
    
    customizeTemplate: (template, variables) => {
        return Object.entries(variables).reduce((t, [key, value]) => {
            return t.replace(`\${${key}}`, value);
        }, template);
    }
};

// Add template validation utilities
export const templateValidation = {
    isValidTemplate: (template) => {
        return typeof template === 'string' && template.includes('${topic}');
    },
    validateKeywords: (keywords) => {
        return Array.isArray(keywords) && keywords.every(k => typeof k === 'string');
    },
    validateEvidence: (evidence) => {
        return Array.isArray(evidence) && evidence.every(e => typeof e === 'string');
    },
    validateTemplateStructure: (template) => {
        return {
            hasTopicPlaceholder: template.includes('${topic}'),
            appropriateLength: template.length >= 20 && template.length <= 200,
            hasProperGrammar: !/\s{2,}/.test(template),
            isWellFormatted: /^[A-Z].*[.!?]$/.test(template)
        };
    },
    validateTemplateCategory: (category, template) => {
        const categoryKeywords = {
            technology: ['digital', 'software', 'hardware', 'system', 'network'],
            business: ['market', 'revenue', 'customer', 'strategy', 'growth'],
            science: ['research', 'study', 'analysis', 'data', 'evidence']
        };
        return categoryKeywords[category]?.some(keyword => 
            template.toLowerCase().includes(keyword)
        );
    }
};

