// Helper functions for template processing
export const templateHelpers = {
    formatNumber: () => Math.floor(Math.random() * 90 + 10),
    formatPercentage: () => Math.floor(Math.random() * 60 + 20),
    formatYear: () => new Date().getFullYear() + Math.floor(Math.random() * 3),
    formatCurrency: (num) => new Intl.NumberFormat('en-US', { 
        style: 'currency', 
        currency: 'USD' 
    }).format(num),
    formatDate: (date = new Date()) => date.toLocaleDateString('en-US', {
        year: 'numeric',
        month: 'long',
        day: 'numeric'
    }),
    formatMetric: (value, unit) => `${value}${unit}`,
    formatRange: (min, max) => `${min}-${max}`,
    formatGrowth: (value) => `${value > 0 ? '+' : ''}${value}%`,
    formatTimeframe: (months) => `${months} ${months === 1 ? 'month' : 'months'}`,
    formatStatistic: (value, precision = 1) => Number(value).toFixed(precision),
    sanitizeInput: (input) => input.replace(/[<>]/g, ''),
    generateId: () => Math.random().toString(36).substr(2, 9),
    formatTimestamp: (date = new Date()) => ({
        iso: date.toISOString(),
        unix: Math.floor(date.getTime() / 1000),
        relative: new Intl.RelativeTimeFormat('en').format(
            Math.floor((date - new Date()) / (1000 * 60 * 60 * 24)), 'day'
        )
    }),
    formatFileSize: (bytes) => {
        const units = ['B', 'KB', 'MB', 'GB', 'TB'];
        let size = bytes;
        let unitIndex = 0;
        while (size >= 1024 && unitIndex < units.length - 1) {
            size /= 1024;
            unitIndex++;
        }
        return `${size.toFixed(2)} ${units[unitIndex]}`;
    },
    generateSlug: (text) => text
        .toLowerCase()
        .replace(/[^\w\s-]/g, '')
        .replace(/\s+/g, '-')
        .replace(/-+/g, '-')
        .trim(),
    formatDuration: (seconds) => {
        const hours = Math.floor(seconds / 3600);
        const minutes = Math.floor((seconds % 3600) / 60);
        const secs = seconds % 60;
        return `${hours}h ${minutes}m ${secs}s`;
    },
    generateUUID: () => {
        return 'xxxxxxxx-xxxx-4xxx-yxxx-xxxxxxxxxxxx'.replace(/[xy]/g, (c) => {
            const r = Math.random() * 16 | 0;
            const v = c === 'x' ? r : (r & 0x3 | 0x8);
            return v.toString(16);
        });
    },
    formatPhoneNumber: (number) => {
        const cleaned = String(number).replace(/\D/g, '');
        const match = cleaned.match(/^(\d{3})(\d{3})(\d{4})$/);
        return match ? `(${match[1]}) ${match[2]}-${match[3]}` : number;
    },
    truncateText: (text, length = 100, suffix = '...') => 
        text.length > length ? `${text.substring(0, length)}${suffix}` : text,
    formatAddress: (address) => ({
        oneLine: `${address.street}, ${address.city}, ${address.state} ${address.zip}`,
        multiLine: `${address.street}\n${address.city}, ${address.state} ${address.zip}`
    })
};

